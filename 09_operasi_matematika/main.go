package main

func main() {
	// aritmatika
	var a = 10
	var b = 12

	var tambah = a + b
	println(tambah)

	var kurang = a - b
	println(kurang)

	var kali = a * b
	println(kali)

	var bagi = a / 3
	println(bagi)

	var modulus = a % 3
	println(modulus)

	// augmented assignments
	a += 3
	println(a)

	a -= 5
	println(a)

	a *= 5
	println(a)

	a /= 10
	println(a)

	a %= 2
	println(a)

	// Unary Operator
	nilai := 3
	nilai++
	println(nilai)
	nilai--
	println(nilai)
	println(-nilai)
	println(+nilai)

	var benar = true
	println(!benar)

}